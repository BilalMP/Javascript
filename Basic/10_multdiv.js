/*
Write a JavaScript program to calculate multiplication and division of two numbers (input from the user).
*/

function MultDiv(a, b) {
    return ("Multiplication of " + a + " and " + b + " is " + a * b) + ("\nDivision of " + a + " and " + b + " is " + a / b + "\n");
}

console.log(MultDiv(5, 6))
console.log(MultDiv(10, 2))
console.log(MultDiv(5, 5))
console.log(MultDiv(25, 5))
console.log(MultDiv(50, 2))
